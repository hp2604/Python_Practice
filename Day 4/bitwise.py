# Compliment Operator : reverse the binary number

print("compliment operator of 12",~12);

# And bitwise 
print(12 & 13);
print(23 &30);

#Or bitwise
print(12 | 13);
print(50 | 234);

# XOR Operator
print(12 ^ 13);
print(20^ 50);

# left shift : Adding the bit
print(10 <<2);

# right shift : Loss of bit
print(10 >>2);



